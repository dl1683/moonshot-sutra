"""S0 Evaluation — validation metrics and generation.

Provides:
1. Byte accuracy (exact match per position within patches)
2. BPB (bits per byte) from CE loss
3. Per-patch entropy analysis
4. Autoregressive byte generation (within-patch, greedy or sampled)
"""

from __future__ import annotations

import math

import torch
import torch.nn.functional as F

from s0_architecture import S0Config, SutraS0


@torch.no_grad()
def compute_metrics(model: SutraS0, byte_ids: torch.Tensor) -> dict:
    """Compute evaluation metrics on a batch.

    Args:
        model: S0 model in eval mode
        byte_ids: (B, T) byte token IDs
    Returns:
        dict with loss, bpb, byte_accuracy, per_position_accuracy
    """
    P = model.cfg.patch_size
    B, T = byte_ids.shape
    N = T // P

    out = model(byte_ids)
    logits = out["logits"]  # (B, N-1, P, 256) — shifted by one patch

    # Targets aligned with shifted logits: patches 1..N-1
    targets = byte_ids.reshape(B, N, P)[:, 1:]  # (B, N-1, P)

    logits_flat = logits.reshape(-1, 256)
    targets_flat = targets.reshape(-1)
    ce_loss = F.cross_entropy(logits_flat, targets_flat).item()
    bpb = ce_loss / math.log(2)

    preds = logits.argmax(dim=-1)  # (B, N-1, P)
    correct = (preds == targets).float()
    byte_accuracy = correct.mean().item()

    per_pos_acc = correct.mean(dim=(0, 1))  # (P,)

    probs = F.softmax(logits, dim=-1)
    entropy = -(probs * probs.clamp(min=1e-8).log()).sum(dim=-1)
    mean_entropy = entropy.mean().item()

    return {
        "loss": ce_loss,
        "bpb": bpb,
        "byte_accuracy": byte_accuracy,
        "per_position_accuracy": per_pos_acc.tolist(),
        "mean_prediction_entropy": mean_entropy,
        "encoder_entropy_mean": out["entropy_scores"].mean().item(),
    }


@torch.no_grad()
def generate_bytes(
    model: SutraS0,
    prompt_bytes: torch.Tensor,
    n_patches: int = 16,
    temperature: float = 1.0,
    top_k: int = 0,
) -> torch.Tensor:
    """Generate bytes autoregressively, patch by patch.

    Args:
        model: S0 model in eval mode
        prompt_bytes: (1, T) byte IDs for context (T must be divisible by P)
        n_patches: number of new patches to generate
        temperature: sampling temperature (0 = greedy)
        top_k: top-k sampling (0 = no filtering)
    Returns:
        generated: (1, n_patches * P) newly generated byte IDs
    """
    assert prompt_bytes.shape[0] == 1, "batch size must be 1 for generation"
    P = model.cfg.patch_size
    device = prompt_bytes.device

    current = prompt_bytes.clone()
    generated = []

    for _ in range(n_patches):
        # Encode all current bytes
        patch_states, byte_states, _, _ = model.encoder(current)

        # Run global reasoner (causal)
        hidden = model.reasoner(patch_states)

        # Get last patch's hidden state for conditioning
        last_hidden = hidden[:, -1:, :]  # (1, 1, d_model)

        # Causal cross-attention context: previous + current
        N = hidden.shape[1]
        if N >= 2:
            prev_hidden = hidden[:, -2:-1, :]
        else:
            prev_hidden = torch.zeros_like(last_hidden)
        nearby = torch.stack([prev_hidden, last_hidden], dim=2)  # (1, 1, 2, d_model)

        # Generate bytes within new patch autoregressively
        cond = model.decoder.cond_proj(last_hidden)  # (1, 1, decoder_dim)
        new_bytes = []

        # First byte: condition only
        x = cond.squeeze(1)  # (1, decoder_dim) → will reshape

        for byte_pos in range(P):
            if byte_pos == 0:
                decoder_input = cond  # (1, 1, decoder_dim)
            else:
                prev_byte = torch.tensor([[new_bytes[-1]]], device=device)
                byte_emb = model.decoder.prev_byte_emb(prev_byte)  # (1, 1, decoder_dim)
                decoder_input = torch.cat([decoder_input, byte_emb], dim=1)

            # Run decoder layers on current sequence
            x = decoder_input
            seq_len = x.shape[1]
            causal_mask = torch.triu(
                torch.ones(seq_len, seq_len, device=device, dtype=torch.bool), diagonal=1
            )

            cross_kv = nearby.reshape(1, -1, model.cfg.d_model) if model.cfg.decoder_cross_attn else None

            for layer in model.decoder.layers:
                x = layer(x, causal_mask, cross_kv)

            x = model.decoder.norm(x)
            logits = model.decoder.lm_head(x[:, -1, :])  # (1, 256)

            # Sample or greedy
            if temperature == 0:
                next_byte = logits.argmax(dim=-1).item()
            else:
                logits = logits / temperature
                if top_k > 0:
                    v, _ = torch.topk(logits, min(top_k, 256))
                    logits[logits < v[:, -1:]] = float('-inf')
                probs = F.softmax(logits, dim=-1)
                next_byte = torch.multinomial(probs, 1).item()

            new_bytes.append(next_byte)

        patch_tensor = torch.tensor([new_bytes], device=device)
        generated.append(patch_tensor)
        current = torch.cat([current, patch_tensor], dim=1)

    return torch.cat(generated, dim=1)


def bytes_to_text(byte_tensor: torch.Tensor) -> str:
    """Convert byte tensor to UTF-8 string, replacing invalid sequences."""
    raw = bytes(byte_tensor.cpu().numpy().tolist())
    return raw.decode("utf-8", errors="replace")


def generation_smoke_test(model: SutraS0, device: torch.device):
    """Quick generation test with English prompt."""
    model.eval()
    P = model.cfg.patch_size

    prompt_text = "The quick brown fox jumps over"
    prompt_raw = prompt_text.encode("utf-8")
    # Pad to patch boundary
    pad_len = P - (len(prompt_raw) % P) if len(prompt_raw) % P != 0 else 0
    prompt_raw = prompt_raw + b'\x00' * pad_len

    prompt_tensor = torch.tensor(list(prompt_raw), dtype=torch.long).unsqueeze(0).to(device)

    print(f"Prompt ({len(prompt_raw)} bytes): {prompt_text}")
    print(f"Generating {8} patches ({8 * P} bytes)...")

    generated = generate_bytes(model, prompt_tensor, n_patches=8, temperature=0.8, top_k=50)
    gen_text = bytes_to_text(generated[0])
    print(f"Generated: {gen_text.encode('ascii', errors='replace').decode('ascii')}")
    print(f"Raw bytes: {generated[0].tolist()}")


def eval_checkpoint(ckpt_path: str, data_dir: str = "data/shards_bytes_full",
                    n_eval_batches: int = 100, gen_prompts: int = 3):
    """Full evaluation on a trained checkpoint."""
    import sys
    sys.path.insert(0, __file__.rsplit("/", 1)[0] if "/" in __file__ else ".")
    from s0_training import ByteShardDataset
    from torch.utils.data import DataLoader
    from pathlib import Path

    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"Device: {device}")

    ckpt = torch.load(ckpt_path, map_location=device, weights_only=False)
    model_cfg = ckpt["model_cfg"]
    model = SutraS0(model_cfg).to(device)
    model.load_state_dict(ckpt["model"])
    model.eval()

    step = ckpt.get("step", "?")
    print(f"Loaded checkpoint from step {step}")
    counts = model.count_parameters()
    print(f"Parameters: {counts['total']:,} ({counts['total']/1e6:.1f}M)")

    # Metrics on eval shards
    all_shards = sorted(Path(data_dir).glob("*.bin"))
    n_shards = len(all_shards)
    n_eval = min(2, max(1, n_shards // 10))
    eval_range = (n_shards - n_eval, n_shards)

    eval_dataset = ByteShardDataset(data_dir, 4096, model_cfg.patch_size, shard_range=eval_range)
    eval_loader = DataLoader(eval_dataset, batch_size=4, shuffle=False, drop_last=True)

    print(f"\n=== Eval Metrics ({n_eval} held-out shards, {n_eval_batches} batches) ===")
    total_loss = 0.0
    total_tokens = 0
    P = model_cfg.patch_size
    pos_correct = torch.zeros(P, device=device)
    pos_total = torch.zeros(P, device=device)

    for i, batch in enumerate(eval_loader):
        if i >= n_eval_batches:
            break
        byte_ids = batch.to(device)
        B, T = byte_ids.shape
        N = T // P

        with torch.amp.autocast("cuda", dtype=torch.bfloat16):
            metrics = compute_metrics(model, byte_ids)

        predicted_bytes = B * (T - P)
        total_loss += metrics["loss"] * predicted_bytes
        total_tokens += predicted_bytes

        out = model(byte_ids)
        targets = byte_ids.reshape(B, N, P)[:, 1:]
        preds = out["logits"].argmax(dim=-1)
        for p in range(P):
            pos_correct[p] += (preds[:, :, p] == targets[:, :, p]).sum()
            pos_total[p] += targets[:, :, p].numel()

    avg_loss = total_loss / max(total_tokens, 1)
    avg_bpb = avg_loss / math.log(2)
    byte_acc = pos_correct.sum().item() / max(pos_total.sum().item(), 1)
    pos_acc = (pos_correct / pos_total.clamp(min=1)).cpu().tolist()

    print(f"  Eval BPB: {avg_bpb:.3f}")
    print(f"  Byte accuracy: {byte_acc:.4f}")
    print(f"  Per-position: {' '.join(f'p{i}={a:.4f}' for i, a in enumerate(pos_acc))}")

    # Generation
    print("\n=== Generation Samples ===")
    prompts = [
        "The quick brown fox",
        "In the court of appeals,",
        "The species was first described",
    ]
    for prompt_text in prompts[:gen_prompts]:
        prompt_raw = prompt_text.encode("utf-8")
        pad_len = P - (len(prompt_raw) % P) if len(prompt_raw) % P != 0 else 0
        prompt_raw = prompt_raw + b'\x00' * pad_len
        prompt_tensor = torch.tensor(list(prompt_raw), dtype=torch.long).unsqueeze(0).to(device)

        generated = generate_bytes(model, prompt_tensor, n_patches=16, temperature=0.8, top_k=50)
        gen_text = bytes_to_text(generated[0])
        print(f"  Prompt: \"{prompt_text}\"")
        print(f"  Output: \"{gen_text[:120]}\"")
        print()

    return {"eval_bpb": avg_bpb, "byte_acc": byte_acc, "pos_acc": pos_acc}


def main():
    import argparse
    parser = argparse.ArgumentParser()
    parser.add_argument("--checkpoint", type=str, default=None,
                        help="Path to checkpoint .pt file")
    parser.add_argument("--data-dir", type=str, default="data/shards_bytes_full")
    parser.add_argument("--batches", type=int, default=100)
    args = parser.parse_args()

    if args.checkpoint:
        eval_checkpoint(args.checkpoint, args.data_dir, args.batches)
    else:
        cfg = S0Config()
        model = SutraS0(cfg)
        device = torch.device("cpu")

        print("=== Metrics on random data (untrained) ===")
        byte_ids = torch.randint(0, 256, (2, 256))
        metrics = compute_metrics(model, byte_ids)
        for k, v in metrics.items():
            if isinstance(v, list):
                print(f"  {k}: {[f'{x:.4f}' for x in v]}")
            else:
                print(f"  {k}: {v:.4f}")

        print(f"\nExpected BPB for random: {math.log(256) / math.log(2):.2f} (= 8.0)")
        print(f"Expected accuracy for random: {1/256:.4f} (= 0.0039)")

        print("\n=== Generation smoke test (untrained) ===")
        generation_smoke_test(model, device)


if __name__ == "__main__":
    main()
